const u8 * FontFind(char c, FontType_Enum font, FontInfo_Struct *FontInfo)
{
  u16 i;
  if(font >= FONT_TYPE_NUM)
    return NULL;
  for(i=0; i<FontInfoTable[font].charNum; i++)
  {
    if(FontInfoTable[font].info[i].charCode == c)
    {
      FontInfo->charCode = FontInfoTable[font].info[i].charCode;
      FontInfo->width = FontInfoTable[font].info[i].width;
      FontInfo->height = FontInfoTable[font].info[i].height;
      FontInfo->len = FontInfoTable[font].info[i].len;
      FontInfo->dataOffset = FontInfoTable[font].info[i].dataOffset;
      return &FontInfoTable[font].data[FontInfoTable[font].info[i].dataOffset];
    }
  }
  return NULL;
}


